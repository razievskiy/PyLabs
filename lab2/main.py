# мультипарадигмальні мови програмування , лаб 2 , Разієвський Ілля

TEMPLATE = 'Enter {} value '
print('Парадигмальні мови прогр , лаба 2')
print('Разіэвський Ілля')

x = int(input(TEMPLATE.format('x')))

y = float(input(TEMPLATE.format('y')))

z = float(input(TEMPLATE.format('z')))

result = ((z - 23.1) * (pow(2, x) + 2.2)) / (y - 21.1) - 3.2
print(result)
